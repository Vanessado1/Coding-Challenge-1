//Task 1: Variables
let employeeName = 'Mary';
console.log(`Employee Name: ${employeeName}, type: ${typeof employeeName}`);// Output: Employee Name: Mary, type: string
const employeeID = 100;
console.log(`Employee ID: ${employeeID}, type: ${typeof employeeID}`);// Output: Employee ID: 100, type: number
var isActive = true;
console.log(`isActive: ${isActive}, type: ${typeof isActive}`);// Output: isActive: true, type: boolean

//Task 2: Primitive Data Types
let productName = 'Ducky';
console.log(`Product Name: ${productName}, type: ${typeof productName}`);// Output: Product Name: Ducky, type: string
const productPrice = 12.99;
console.log(`Product Price: ${productPrice}, type: ${typeof productPrice}`);// Output: Product Price: 12.99, type: number
var isAvailable = true;
console.log(`isAvailable: ${isAvailable}, type: ${typeof isAvailable}`);// Output: isAvailable: true, type: boolean

//Task 3: Number Data Type
let accountBalance = 1500;
console.log(`Initial Balance: ${accountBalance}`);// Output: Initial Balance: 1500  
